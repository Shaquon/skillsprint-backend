const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      enum: ["user", "mentor"],
      default: "user",
    },
    // Add more fields as needed
  },
  { timestamps: true }
);

const User = mongoose.model("User", userSchema);

module.exports = User;
